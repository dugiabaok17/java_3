    public void informationStudent(String id) {
        for (String list : new RepositoryStudent().getListId()) {

            if (id.equalsIgnoreCase(list)) {
                List<PointCustom> listOne = ss.getOne(id);
                this.txtID.setText(listOne.get(0).getIdStudent());
                this.txtName.setText(listOne.get(0).getNameStudent());
                this.txtEnglish.setText(listOne.get(0).getEnglish()+"");
                this.txtJava.setText(listOne.get(0).getJava()+"");
                this.txtPhp.setText(listOne.get(0).getPhp()+"");

            }
        }
    }

